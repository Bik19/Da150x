def update_visuals(single):
    pygame.init()
    SCREEN = pygame.display.set_mode((WINDOW_WIDTH, WINDOW_HEIGHT))
    SCREEN.fill(WHITE)
    global isDone
    while True:
        drawGrid(SCREEN) 
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                pygame.quit()
                sys.exit()
        pygame.display.update()
        # clock.tick(60)
        if single:
            if isDone == True:
                isDone = False
                return 0

def scanEnvironment(u, agent):
    global matrix
    first = True
    newCost = 0
    for v in agent.neighbors.get(u, []):
            if not (0 <= v[0] < len(matrix) and 0 <= v[1] < len(matrix[0])):
                continue
            if matrix[v[1]][v[0]] == 0 and agent.compute_cost[(u, v)] == float('inf'):
                newCost = 1
                if first:
                    agent.km += agent.h(agent.S_last, agent.S_start)
                    first = False
            elif matrix[v[1]][v[0]] >= 1 and agent.compute_cost[(u, v)] != float('inf'):
                newCost = float('inf')
                if first:
                    agent.km += agent.h(agent.S_last, agent.S_start)
                    first = False
            if first == False:
                agent.S_last = agent.S_start
                for neighbor in agent.neighbors.get(v, []):
                    agent.compute_cost[(neighbor, v)] = newCost
                    agent.compute_cost[(v, neighbor)] = newCost
                    c_new = newCost  
                    c_old = agent.compute_cost[(neighbor, v)]
                    if c_old >= c_new:
                        if u != agent.S_goal:
                            agent.rhs[u] = min(agent.rhs[u], c_new + agent.g[v])
                    elif agent.rhs[u] == c_old + agent.g[v]:
                        if u != agent.S_goal:
                            agent.rhs[u] = agent.pickMin(agent.S_start)[1]
            if u != agent.S_goal:
                agent.rhs[u] = agent.pickMin(u)[0]
            agent.updateVertex(u)
    agent.computeShortestPath()




def run_pathfinding(agent, id):
    global matrix, stepCounter, isDone, start_timer

    agent.S_last = agent.S_start
    agent.initialize()
    agent.computeShortestPath()

    while agent.S_start != agent.S_goal:
        nextMove = agent.pickMin(agent.S_start)[1]
        x, y = nextMove[0], nextMove[1]
        with matrix_lock:
            if matrix[y][x] == 0:
                tempPos = agent.pos
                agent.pos = nextMove
                agent.S_start = nextMove
                stepCounter += 1

                matrix[y][x] = 2
                matrix[tempPos[1]][tempPos[0]] = 0
        scanEnvironment(agent.S_start, agent)
        clock.tick(60)

    isDone = True
    matrix[agent.S_start[1]][agent.S_start[0]] = 0
    print(f'Agent {id} found a path!')

def changeMatrix(matrix):
    half = len(matrix)//2
    full = len(matrix) - 1

    quadrants = [
        (range(0, half), range(0, half)),
        (range(0, half), range(half, full)),
        (range(half, full), range(0, half)),
        (range(half, full), range(half, full)),
    ]
    # random.seed(41)
    while True:
        for rangeX, rangeY in quadrants:
            y = random.choice(list(rangeX))
            x = random.choice(list(rangeY))
            if matrix[y][x] == 0:
                matrix[y][x] = 1

            y = random.choice(list(rangeX))
            x = random.choice(list(rangeY))
            if matrix[y][x] == 1:
                matrix[y][x] = 0
        
        time.sleep(0.06)


def multipleAgentsMain(numAgents):
    global matrix
    setMatrix()
    s_g = (len(matrix)-1, len(matrix)-1)
    threads = []
    for i in range(numAgents):
        id = i
        while True:
            x = random.randint(0, len(matrix)-25)
            y = random.randint(0, len(matrix)-1)
            if(matrix[x][y] == 0):
                start = (y, x)
                break
        agent0 = DstarLite.Dstar(matrix, start, (s_g))
        agent0.startFinding()
        dstar_thread0 = threading.Thread(target=run_pathfinding, args=(agent0, id), daemon=True)
        threads.append(dstar_thread0)
    for thread in threads:
        thread.start()
    update_visuals(single = False)
    for thread in threads:
        thread.join()
   

