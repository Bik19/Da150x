def changeMatrix(matrix):
    half = len(matrix)//2
    full = len(matrix) - 1

    quadrants = [
        (range(0, half), range(0, half)),
        (range(0, half), range(half, full)),
        (range(half, full), range(0, half)),
        (range(half, full), range(half, full)),
    ]
    while True:
        for rangeX, rangeY in quadrants:
            y = random.choice(list(rangeX))
            x = random.choice(list(rangeY))
            if matrix[y][x] == 0:
                matrix[y][x] = 1
            
            y = random.choice(list(rangeX))
            x = random.choice(list(rangeY))
            if matrix[y][x] == 1:
                matrix[y][x] = 0
        time.sleep(0.1) 


