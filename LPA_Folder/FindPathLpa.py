def scanEnvironment(u, agent):
    global matrix
    for v in agent.neighbors.get(u, []):
            if not (0 <= v[0] < len(matrix) and 0 <= v[1] < len(matrix[0])):
                continue
            if matrix[v[1]][v[0]]  == 0 and agent.compute_cost[(u, v)] == float('inf'):
                for neighbor in agent.neighbors.get(v, []):
                    agent.compute_cost[(neighbor, v)] = 1
                    agent.compute_cost[(v, neighbor)] = 1
            elif matrix[v[1]][v[0]] >= 1 and agent.compute_cost[(u, v)] != float('inf'):
                for neighbor in agent.neighbors.get(v, []):
                    agent.compute_cost[(neighbor, v)] = float('inf')
                    agent.compute_cost[(v, neighbor)] = float('inf')
                    agent.g[v] = float('inf')
                    


